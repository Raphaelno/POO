// POO(Programação Orientada a objetos)
// É um paradigma de desenvolvimento utilizado
// na maioria dos sistemas atuais

// Representar cada elemento no mundo real em um objeto

// Pilares da POO(Programação Orientada a Objetos)
 
// 1. Herança
// 2. Polimorfismo
// 3. Encapsulamento
// 4. Abstração

// 1. É a capacidade de um objeto ser idealizado
// baseado em outro objeto, ou seja, se no seu
// projeto principal, que podemos chamar de pai,
// existem atributos, que são as propriedades, e
// os métodos, que são as funções, eles podem
// ser extendidos para um novo objeto, o filho
// (O objeto, pai, pode ter sido extendido de
// outro objeto)

// 2. É a capacidade de um objeto, que sejam
// compatíveis, se passar por outro em 
// determina circunstância. Depois que um objeto
// é herdado, apesar de trazer todas as características
// do objeto pai, uma ou mais características precisam
// ser reescritas por completo. É a reescrita de
// um método herdado de uma classe sem utilizar
// nenhum comportamento da classe pai para esse
// método.

// 3. Somente a capacidade de esconder 
// detalhes da implementação do objeto
// expondo só o que deve ser acessado
// publicamente(criar uma caixa preta)

// 4. Representa um objeto de uma forma abstrata
// que seja obrigatoriamente herdado de outras classes
// (Implementação nas classes filhas)
// Implementação é visto sendo chamado como
// Template ou identidade, na programação é chamada
// de SUPERCLASSE. E não pode ser criado um objeto
// diretamente dessa classe

